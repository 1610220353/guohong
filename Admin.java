package com.sample;


public class Admin {

  private long adminId;
  private String adminAccount;
  private String adminPwd;
  private String adminAtate;
  private String adminRight;
  private String adminName;


  public long getAdminId() {
    return adminId;
  }

  public void setAdminId(long adminId) {
    this.adminId = adminId;
  }


  public String getAdminAccount() {
    return adminAccount;
  }

  public void setAdminAccount(String adminAccount) {
    this.adminAccount = adminAccount;
  }


  public String getAdminPwd() {
    return adminPwd;
  }

  public void setAdminPwd(String adminPwd) {
    this.adminPwd = adminPwd;
  }


  public String getAdminAtate() {
    return adminAtate;
  }

  public void setAdminAtate(String adminAtate) {
    this.adminAtate = adminAtate;
  }


  public String getAdminRight() {
    return adminRight;
  }

  public void setAdminRight(String adminRight) {
    this.adminRight = adminRight;
  }


  public String getAdminName() {
    return adminName;
  }

  public void setAdminName(String adminName) {
    this.adminName = adminName;
  }

}
